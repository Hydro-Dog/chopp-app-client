export function toScreamingSnakeCase(input: string) {
  // Преобразуем строку, добавляя подчеркивание перед каждой заглавной буквой и переводя все в верхний регистр
  return input
    .replace(/(.)([A-Z])/g, '$1_$2') // Добавляем '_' между строчной и заглавной буквами
    .toUpperCase(); // Преобразуем всю строку в верхний регистр
}
